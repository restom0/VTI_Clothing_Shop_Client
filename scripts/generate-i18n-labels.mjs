import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

const FALLBACK_LANGUAGE = "en";
const LANGUAGE_STORAGE_KEY = "vti-shop-language";
const SUPPORTED_LANGUAGES = [
  { code: "en", labelKey: "language.en" },
  { code: "ca", labelKey: "language.ca" },
  { code: "it", labelKey: "language.it" },
  { code: "es", labelKey: "language.es" },
  { code: "de", labelKey: "language.de" },
  { code: "fr", labelKey: "language.fr" },
];

const findSeparatorIndex = (line) => {
  let escaped = false;

  for (let index = 0; index < line.length; index += 1) {
    const character = line[index];

    if (escaped) {
      escaped = false;
      continue;
    }

    if (character === "\\") {
      escaped = true;
      continue;
    }

    if (character === "=" || character === ":") {
      return index;
    }
  }

  return -1;
};

const decodePropertiesValue = (value) =>
  value
    .replace(/\\u([\dA-Fa-f]{4})/g, (_, code) =>
      String.fromCharCode(parseInt(code, 16))
    )
    .replace(/\\n/g, "\n")
    .replace(/\\r/g, "\r")
    .replace(/\\t/g, "\t")
    .replace(/\\f/g, "\f")
    .replace(/\\([:=#!\\])/g, "$1");

const parseProperties = (source) =>
  source.split(/\r?\n/).reduce((labels, rawLine) => {
    const line = rawLine.trim();

    if (!line || line.startsWith("#") || line.startsWith("!")) {
      return labels;
    }

    const separatorIndex = findSeparatorIndex(line);

    if (separatorIndex === -1) {
      labels[decodePropertiesValue(line)] = "";
      return labels;
    }

    const key = decodePropertiesValue(line.slice(0, separatorIndex).trim());
    const value = decodePropertiesValue(line.slice(separatorIndex + 1).trim());
    labels[key] = value;

    return labels;
  }, {});

const readLabels = async (language) => {
  const filePath = path.join(
    rootDir,
    "src",
    "i18n",
    "locales",
    language,
    "labels.properties"
  );
  const source = await readFile(filePath, "utf8");

  return parseProperties(source);
};

const formatArray = (items) => JSON.stringify(items, null, 2);

const formatTranslations = (translations) =>
  Object.entries(translations)
    .map(([language, labels]) => {
      const body = Object.entries(labels)
        .map(([key, value]) => `    ${JSON.stringify(key)}: ${JSON.stringify(value)},`)
        .join("\n");

      return `  ${language}: {\n${body}\n  },`;
    })
    .join("\n");

const validateTranslations = (translations) => {
  const fallbackKeys = Object.keys(translations[FALLBACK_LANGUAGE]).sort();
  const errors = [];

  for (const { code } of SUPPORTED_LANGUAGES) {
    const keys = Object.keys(translations[code]).sort();
    const missing = fallbackKeys.filter((key) => !keys.includes(key));
    const extra = keys.filter((key) => !fallbackKeys.includes(key));

    if (missing.length || extra.length) {
      errors.push(
        `${code}: missing [${missing.join(", ")}], extra [${extra.join(", ")}]`
      );
    }
  }

  if (errors.length) {
    throw new Error(`i18n label keys are out of sync:\n${errors.join("\n")}`);
  }
};

const generateLabelsModule = (translations) => `// This file is generated by scripts/generate-i18n-labels.mjs.
// Do not edit this file directly. Edit src/i18n/locales/*/labels.properties.

export const FALLBACK_LANGUAGE = ${JSON.stringify(FALLBACK_LANGUAGE)};
export const LANGUAGE_STORAGE_KEY = ${JSON.stringify(LANGUAGE_STORAGE_KEY)};

export const SUPPORTED_LANGUAGES = ${formatArray(SUPPORTED_LANGUAGES)};

export const translations = {
${formatTranslations(translations)}
};
`;

const run = async () => {
  const translations = {};

  for (const { code } of SUPPORTED_LANGUAGES) {
    translations[code] = await readLabels(code);
  }

  validateTranslations(translations);

  const outputDir = path.join(rootDir, "src", "i18n", "generated");
  const outputPath = path.join(outputDir, "labels.js");

  await mkdir(outputDir, { recursive: true });
  await writeFile(outputPath, generateLabelsModule(translations), "utf8");

  console.log(
    `Generated ${path.relative(rootDir, outputPath)} for ${SUPPORTED_LANGUAGES.length} languages.`
  );
};

run().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
